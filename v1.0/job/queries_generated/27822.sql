SELECT 
    t.title AS movie_title,
    a.name AS actor_name,
    r.role AS actor_role,
    c.name AS company_name,
    k.keyword AS movie_keyword,
    COUNT(DISTINCT mi.info) AS info_count,
    STRING_AGG(DISTINCT mi.info, ', ') AS additional_info,
    ARRAY_AGG(DISTINCT title.production_year) AS production_years
FROM 
    aka_title t
JOIN 
    cast_info ci ON t.id = ci.movie_id
JOIN 
    aka_name a ON ci.person_id = a.person_id
JOIN 
    role_type r ON ci.role_id = r.id
JOIN 
    movie_companies mc ON t.id = mc.movie_id
JOIN 
    company_name c ON mc.company_id = c.id
JOIN 
    movie_keyword mk ON t.id = mk.movie_id
JOIN 
    keyword k ON mk.keyword_id = k.id
LEFT JOIN 
    movie_info mi ON t.id = mi.movie_id
WHERE 
    t.production_year >= 2000
    AND a.name ILIKE '%Smith%' -- Actors having "Smith" in their name
    AND ci.nr_order < 5 -- First five cast members only
GROUP BY 
    t.title, a.name, r.role, c.name, k.keyword
ORDER BY 
    COUNT(DISTINCT mi.info) DESC, -- Sort by number of additional info available
    t.title;
